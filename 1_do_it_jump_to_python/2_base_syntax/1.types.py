# NUMBERS...

number_int = 1
number_float = 1.03123412
cutted_float = "%0.2f" % number_float

print('number_int', number_int) # 1
print('type(number_int)', type(number_int)) # <class 'int'>

print('number_float', number_float) # 1.03123412
print('type(number_float)', type(number_float)) # <class 'float'>

print('cutted_float', cutted_float) # 1.03
print('type(cutted_float)', type(cutted_float), '\n') # <class 'str'>
