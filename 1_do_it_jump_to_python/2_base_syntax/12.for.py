# For

string_list = ['one', 'two', 'three']
for string in string_list:
    print(string)
        # one
        # two
        # three
        
set_list = [(1, 2), (3, 4), (5, 6)]
for (first, last) in set_list:
    print(first, last)
        # 1 2
        # 3 4
        # 5 6

for i in range(2, 5):
    print(i)
        # 2
        # 3
        # 4